            <h2 class="titulo-article-conteudo">Los Angeles 2049</h2>
            <article>
                <p>
                    A história se passa em Los Angeles, no ano de 2049, trinta anos após os eventos do filme original. A sociedade é agora ainda mais dividida, com replicantes Nexus-9 obedecendo aos humanos e caçadores de replicantes, conhecidos como blade runners, ainda em busca dos modelos mais antigos que se rebelaram contra seus criadores. O filme segue com <strong>K</strong>, um blade runner interpretado por  
                    <a href="https://www.google.com/search?q=ryan+gosling&rlz=1C1FKPE_pt-BRBR1120BR1120&oq=ryan+gos&gs_lcrp=EgZjaHJvbWUqDQgAEAAY4wIYsQMYgAQyDQgAEAAY4wIYsQMYgAQyCggBEC4YsQMYgAQyBggCEEUYOTIPCAMQABhDGLEDGIAEGIoFMgoIBBAuGLEDGIAEMgoIBRAAGLEDGIAEMgcIBhAAGIAEMgcIBxAAGIAEMgcICBAAGIAEMgcICRAAGIAE0gEIMzE5N2owajeoAgiwAgE&sourceid=chrome&ie=UTF-8" class="link-texto" target="_blank" rel="noopener noreferrer"> 
