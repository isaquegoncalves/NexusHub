            <h2 class="titulo-article-conteudo">Do FaceMash ao Facebook</h2>
            <article>
                <p>
                    Conforme o Facebook cresce em popularidade e se expande para outras universidades, a visão de <a href="https://www.google.com/search?q=Mark+Zuckerberg" class="link-texto" target="_blank" rel="noopener noreferrer">
                    <strong>Zuckerberg</strong>
                    <sup>
                        <i class="fa-solid fa-arrow-up-right-from-square"></i>
                    </sup>
