            <h2 class="titulo-article-conteudo">A ascensão do Facebook</h2>
            <article>
                <p>
                   Dirigido por <a href="https://www.google.com/search?q=David+Fincher" class="link-texto" target="_blank" rel="noopener noreferrer">
                        David Fincher
                        <sup>
                            <i class="fa-solid fa-arrow-up-right-from-square"></i>
                        </sup>
                    </a> e lançado em 2010, é um drama biográfico que narra a fundação do Facebook, uma das redes sociais mais influentes do mundo. Baseado no livro <a href="https://www.google.com/search?q=The+Accidental+Billionaires" class="link-texto" target="_blank" rel="noopener noreferrer">
                        "The Accidental Billionaires"
                        <sup>
                            <i class="fa-solid fa-arrow-up-right-from-square"></i>
                        </sup>
                    </a> de Ben Mezrich, o filme oferece uma visão intrigante das complexidades e controvérsias que cercaram o nascimento da gigante tecnológica. Com um roteiro brilhante de 
                    <a href="https://www.google.com/search?q=Aaron+Sorkin" class="link-texto" target="_blank" rel="noopener noreferrer">
                        Aaron Sorkin
                        <sup>
                            <i class="fa-solid fa-arrow-up-right-from-square"></i>
                        </sup>
                    </a>, <strong>"A Rede Social"</strong> mergulha profundamente nos temas de ambição, amizade e traição.
                </p>
            </article>

            <h2 class="titulo-article-conteudo">A Jornada de Zuckerberg</h2>
            <article>
                <p>
                    O filme começa em 2003, com o estudante de Harvard, <a href="https://www.google.com/search?q=Jesse+Eisenberg" class="link-texto" target="_blank" rel="noopener noreferrer">
                    Jesse Eisenberg
                    <sup>
                        <i class="fa-solid fa-arrow-up-right-from-square"></i>
                    </sup>
                    </a>, criando um site chamado "FaceMash" após um rompimento com sua namorada <a href="https://www.google.com/search?q=Rooney+Mara" class="link-texto" target="_blank" rel="noopener noreferrer">
                        Rooney Mara
                        <sup>
                            <i class="fa-solid fa-arrow-up-right-from-square"></i>
                        </sup>
                    </a>. O site, que permitia aos usuários avaliar a atratividade de colegas, rapidamente atrai a atenção da universidade, levando <a href="https://www.google.com/search?q=Mark+Zuckerberg" class="link-texto" target="_blank" rel="noopener noreferrer">
                        Zuckerberg
                        <sup>
                            <i class="fa-solid fa-arrow-up-right-from-square"></i>
                        </sup>
                    </a> a enfrentar consequências disciplinares.
                </p>

                <p>
                    Inspirado pelo sucesso de "FaceMash" e a ideia de criar uma rede social exclusiva para estudantes de Harvard, <a href="https://www.google.com/search?q=Mark+Zuckerberg" class="link-texto" target="_blank" rel="noopener noreferrer">
                    Zuckerberg
                    <sup>
                        <i class="fa-solid fa-arrow-up-right-from-square"></i>
                    </sup>
                    </a> começa a trabalhar no que eventualmente se tornará o Facebook. Ele recebe apoio financeiro de seu amigo <a href="https://www.google.com/search?q=Andrew+Garfield" class="link-texto" target="_blank" rel="noopener noreferrer">
                        Andrew Garfield
                        <sup>
                            <i class="fa-solid fa-arrow-up-right-from-square"></i>
                        </sup>
                    </a>, que se torna o co-fundador da empresa.
                </p>
            </article>

            
